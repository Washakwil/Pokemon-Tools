	public static Pokemon getPokemon(int pokedexNo, int form) {
		for(int i = 0; i < POKEMON.length; i++) {
			Pokemon pokemon = POKEMON[i];
			if(pokemon.dexNo == pokedexNo) {
				for(int f = 1, index = i + f; f <= form && index < POKEMON.length; i++, index = i + f) {
					if(POKEMON[index].dexNo == pokedexNo) pokemon = POKEMON[index];
					else break;
				}
				return pokemon;
			}
		}
		return null;
	}

